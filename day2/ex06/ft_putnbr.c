#include <unistd.h>

void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		if (nb == -2147483648)
		{	
			ft_putchar('2');
			nb = nb + 2000000000;
		}
		nb *= -1;
	}
	if (nb >= 10 || nb <= -10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		if (nb >= 0)
			ft_putchar(nb + '0');
	}
}
