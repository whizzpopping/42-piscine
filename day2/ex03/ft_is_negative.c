#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	if (n < 0)
	{
		putchar('N');
	}
	if (n >= 0)
	{
		putchar('P');
	}
}
