int	ft_atoi(char *str)
{
	int i;
	int nbr;
	int neg;

	i = 0;
	nbr = 0;
	neg = 1;
	while (str[i] != '\0')
	{
		if (*str == '\n' || *str == '\r' || *str == '\v' || *str == '\f')
			str++;
		if (str[i] == '-')
			neg = -1;
		if (str[i] == '\t' || str[i] == '-' || str[i] == '+' || str[i] == '-')
			i++;
		while (str[i] >= '0' && str[i] <= '9')
		{
			nbr = nbr * 10 + (str[i] - 48);
			i++;
		}
	}
	return (nbr * neg);
}
